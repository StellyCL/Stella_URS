bool simUartWrite(const char* outBuff, uint32_t outLen) //funkcija vraca boolean vrijednost ovisno o uspjehu
{
	return HAL_UART_Transmit(&huart1, (const uint8_t*)outBuff, outLen, 1000) == HAL_OK ? true : false ;
}
bool simUartRead(char* inBuff, uint32_t inSize, uint32_t timeout)
{
	memset(inBuff, '\0', inSize); // ocistimo buffer
	return HAL_UART_Receive(&huart1, (uint8_t*)inBuff, inSize-1, timeout) == HAL_OK ? true : false ;
}


bool pcUartWrite(const uint8_t* outBuff, uint32_t outLen)
{
	return HAL_UART_Transmit(&huart2, outBuff, outLen, 1000) == HAL_OK ? true : false ;
}

// Function definition
bool uartPrintf(const char *format, ...)
{
    va_list args; //lista izlazne poruke
    int formatedLen;

    va_start(args, format);

    formatedLen = vsnprintf(pcUartOutBuff, sizeof(pcUartOutBuff), format, args);

    va_end(args);

    bool retval = pcUartWrite(
    	(uint8_t*)pcUartOutBuff,   //usporedujemo velicinu buffera i liste
		formatedLen > sizeof(pcUartOutBuff) ? sizeof(pcUartOutBuff) : formatedLen
	);

    return retval;
}

